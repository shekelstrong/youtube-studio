"""generate_script: генерация сценария."""


async def run(topic: str, duration_min: int = 8, style: str = "educational", tone: str = "engaging") -> dict:
    """Генерирует сценарий.

    Args:
        topic: Тема видео.
        duration_min: Длительность (мин).
        style: educational / story / tutorial / rant / review.
        tone: engaging / serious / humorous.

    Returns:
        Словарь со сценарием.
    """
    word_count = duration_min * 130  # ~130 слов/мин

    return {
        "topic": topic,
        "duration_min": duration_min,
        "style": style,
        "tone": tone,
        "target_word_count": word_count,
        "structure": [
            {
                "section": "HOOK (0:00 - 0:15)",
                "duration_sec": 15,
                "goal": "Зацепить за 5 секунд. Без hook = зритель уходит.",
                "templates": [
                    f"В этом видео я покажу [конкретный результат] за [короткое время].",
                    f"Если ты [боль зрителя] — это видео изменит всё.",
                    f"Большинство делают [ошибку]. Я покажу как правильно.",
                    f"[Провокационное утверждение]. И вот почему.",
                ],
            },
            {
                "section": f"INTRO (0:15 - 0:45)",
                "duration_sec": 30,
                "goal": "Объяснить зачем смотреть, что получит зритель.",
                "templates": [
                    "Сегодня разберём [3-5 пунктов]. В конце — бонус для тех кто досмотрит.",
                    "Это видео для тех, кто [целевая аудитория] и хочет [желаемый результат].",
                ],
            },
            {
                "section": f"ОСНОВНАЯ ЧАСТЬ (0:45 - {duration_min - 1}:00)",
                "duration_sec": (duration_min - 1) * 60 - 45,
                "goal": "Дать ценность. Retention hooks каждые 60-90 сек.",
                "retention_hooks": [
                    "Но и это ещё не всё...",
                    "А теперь самое интересное...",
                    "Секрет в том, что...",
                    "И вот почему это работает...",
                ],
            },
            {
                "section": f"CTA ({duration_min - 1}:00 - {duration_min}:00)",
                "duration_sec": 60,
                "goal": "Подписка, лайк, комментарий, следующее видео.",
                "templates": [
                    "Если было полезно — подпишись, я делаю такие видео каждую неделю.",
                    "Напиши в комментах [вопрос] — отвечу в следующем видео.",
                ],
            },
        ],
        "principles": [
            "Hook в первые 5 секунд — иначе зритель уходит",
            "Retention hook каждые 60-90 секунд (смена визуала, вопрос, обещание)",
            "Конкретика > абстракция: цифры, примеры, кейсы",
            "Структура видна зрителю: 'в этом видео 3 пункта' = зритель досматривает",
        ],
    }
