"""suggest_topics: генерация идей для видео."""


# Шаблоны идей по нишам
NICHE_IDEAS = {
    "ai": ["5 AI-инструментов, которые заменят твою работу", "Как я автоматизировал X за 1 час с Claude", "Сравнение: Claude vs GPT-4o vs Gemini для задачи Y", "3 способа использовать AI для заработка", "AI написал код — что сломалось и почему"],
    "tech": ["Как я выбирал между X и Y", "5 ошибок junior-разработчика", "Почему я ушёл с X работы", "Мой стек 2026 года", "Docker за 10 минут: реальный кейс"],
    "freelance": ["Как я нашёл первого клиента за 7 дней", "Сколько реально платят на Kwork в 2026", "3 ниши, где новичку легко стартовать", "Как я автоматизировал Kwork через AI", "Почему фриланс это не про 'свободу'"],
    "business": ["Мой путь от 0 до X за Y месяцев", "Что я понял после 100 клиентов", "3 метрики, которые реально важны", "Как я упал и поднялся", "Самые дорогие ошибки в моём бизнесе"],
    "default": ["Личный опыт: что я сделал и что получилось", "Разбор кейса: вход → результат", "Топ-5 ошибок которые я совершил", "Сравнение: 3 способа сделать X", "Шаги которые я прошёл чтобы сделать Y"],
}


async def run(niche: str, count: int = 10) -> dict:
    """Генерирует идеи.

    Args:
        niche: Ниша канала.
        count: Кол-во идей.

    Returns:
        Словарь с идеями.
    """
    niche_lower = niche.lower()
    ideas = None
    for k in NICHE_IDEAS.keys():
        if k in niche_lower:
            ideas = NICHE_IDEAS[k]
            break
    if not ideas:
        ideas = NICHE_IDEAS["default"]

    # Дополняем шаблонными до нужного количества
    templates = [
        f"Мой опыт: {niche} за последний год",
        f"3 ошибки в {niche}, которые стоили мне X",
        f"Сравнение: подход A vs подход B в {niche}",
        f"Шаги: как сделать X в {niche}",
        f"Разбор: почему X не работает в {niche}",
        f"Топ-5 инструментов для {niche}",
    ]
    while len(ideas) < count:
        ideas.append(templates[len(ideas) % len(templates)])

    return {
        "niche": niche,
        "ideas": ideas[:count],
        "principles": [
            "Идея должна решать конкретную боль зрителя",
            "Title с цифрой = +30% CTR",
            "Конкретный результат > общее описание",
            "Проверяй что идея не была сделана 1000 раз (добавь уникальный угол)",
        ],
    }
